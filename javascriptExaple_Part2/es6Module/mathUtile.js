// use "export" kye word
export const PI = 3.14159;
export function getcircle(raduis) {
  return 2 * PI * raduis;
}

export function getarea(raduis) {
  return PI * raduis * raduis;
}
export function getvolume(raduis) {
  return 4 * PI * raduis * raduis;
}
